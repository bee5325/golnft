    >
      <p class="m-auto whitespace-pre-line">{{notification.msg}}</p>
      <carbon-close @click="clearNotification" class="cursor-pointer" />
    </div>
      <p class="inline-block">Contract address</p><input class="font-bold mx-2" v-model="contractAddress" />
